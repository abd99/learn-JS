// if condition
if (true) {
    console.log("Inside if block")
}

// if with else
if (false) {
    console.log("Inside if block")
} else {
    console.log("Inside else block")
}

// else if condition
if (false) {
    console.log("Inside if block")
} else if (true) {
    console.log("Inside else if block")
} else {
    console.log("Inside else block")
}
